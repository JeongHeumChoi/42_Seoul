int	plus(int op1, int op2)
{
	return (op1 + op2);
}

int	minus(int op1, int op2)
{
	return (op1 - op2);
}

int	div(int op1, int op2)
{
	return (op1 / op2);
}

int	mult(int op1, int op2)
{
	return (op1 * op2);
}

int	modulo(int op1, int op2)
{
	return (op1 % op2);
}
