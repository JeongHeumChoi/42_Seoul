int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	cnt;

	cnt = 0;
	if (n == 0)
		return (0);
	while (cnt < n - 1 && *s1 && *s2)
	{
		if (*s1 != *s2)
			return (*s1 - *s2);
		s1++;
		s2++;
		cnt++;
	}
	return (*s1 - *s2);
}
